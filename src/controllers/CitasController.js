import pool from "../config/db.js";

export const crearCita = async (req, res) => {
  const {
    paciente_id,
    doctor_id,
    consultorio_id,
    fecha,
    hora,
    estado_id,
    usuario_reg,
    peticion_citas_id,
  } = req.body;

  if (
    !paciente_id ||
    !doctor_id ||
    !consultorio_id ||
    !fecha ||
    !hora ||
    !estado_id ||
    !usuario_reg
  ) {
    return res.status(400).json({
      error: "Faltan datos obligatorios",
      lo_que_llego: req.body,
    });
  }

  try {
    const result = await pool.query(
      "INSERT INTO citas (paciente_id, doctor_id, consultorio_id, fecha, hora, estado_id, usuario_reg, peticion_citas_id) VALUES ($1, $2, $3, $4, $5, $6, $7, $8) RETURNING *",
      [
        paciente_id,
        doctor_id,
        consultorio_id,
        fecha,
        hora,
        estado_id,
        usuario_reg,
        peticion_citas_id,
      ],
    );

    res.status(201).json({
      mensaje: "Cita Creada",
      id: result.rows[0].id,
    });
  } catch (error) {
    console.error(error);
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};

export const obtenerCitas = async (req, res) => {
  try {
    const result = await pool.query("SELECT * FROM citas ORDER BY id ASC");
    res.status(200).json(result.rows);
  } catch (error) {
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};
