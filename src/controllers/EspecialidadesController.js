import pool from "../config/db.js";

export const crearEspecialidad = async (req, res) => {
  const { nombre_esp } = req.body;

  if (!nombre_esp) {
    return res.status(400).json({
      error: "Faltan datos: se requiere nombre_esp",
      lo_que_llego: req.body,
    });
  }

  try {
    const result = await pool.query(
      "INSERT INTO especialidades (nombre_esp) VALUES ($1) RETURNING *",
      [nombre_esp],
    );

    res.status(201).json({
      mensaje: "Especialidad Creada",
      id: result.rows[0].id,
    });
  } catch (error) {
    console.error(error);
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};

export const obtenerEspecialidades = async (req, res) => {
  try {
    const result = await pool.query(
      "SELECT * FROM especialidades ORDER BY id ASC",
    );
    res.status(200).json(result.rows);
  } catch (error) {
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};
