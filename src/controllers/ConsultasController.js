import pool from "../config/db.js";

export const crearConsulta = async (req, res) => {
  const { cita_id, diagnostico, receta, usuario_reg } = req.body;

  if (!cita_id || !diagnostico || !receta || !usuario_reg) {
    return res.status(400).json({
      error:
        "Faltan datos: se requiere cita_id, diagnostico, receta y usuario_reg",
      lo_que_llego: req.body,
    });
  }

  try {
    const result = await pool.query(
      "INSERT INTO consultas (cita_id, diagnostico, receta, usuario_reg) VALUES ($1, $2, $3, $4) RETURNING *",
      [cita_id, diagnostico, receta, usuario_reg],
    );

    res.status(201).json({
      mensaje: "Consulta Creada",
      id: result.rows[0].id,
    });
  } catch (error) {
    console.error(error);
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};

export const obtenerConsultas = async (req, res) => {
  try {
    const result = await pool.query("SELECT * FROM consultas ORDER BY id ASC");
    res.status(200).json(result.rows);
  } catch (error) {
    res.status(500).json({ mensaje: "Error", detalle: error.message });
  }
};
